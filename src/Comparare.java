public class Comparare {
    public int max(int x, int y) {
        if (x > y) return x;
        else return y;
    }

    public double max(double x, double y) {
        if (x > y) return x;
        else return y;
    }

    public char max(char x, char y) {
        if (x > y) return x;
        else return y;
    }
}
